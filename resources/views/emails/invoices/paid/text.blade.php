** DESIGNING YOUR TEMPLATE
------------------------------------------------------------

 
** CREATING A GOOD-LOOKING EMAIL IS SIMPLE
------------------------------------------------------------

 Customize your template by clicking on the style editor tabs above. Set your fonts, colors, and styles. After setting your styling is all done you can click here in this area, delete the text, and start adding your own awesome content. 

 
** STYLING YOUR CONTENT
------------------------------------------------------------

 
** MAKE YOUR EMAIL EASY TO READ
------------------------------------------------------------

 After you enter your content, highlight the text you want to style and select the options you set in the style editor in the "_styles_" drop down box. Want to get rid of styling on a bit of text (http://www.mailchimp.com/kb/article/im-using-the-style-designer-and-i-cant-get-my-formatting-to-change) , but having trouble doing it? Just use the "_remove formatting_" button to strip the text of any formatting and reset your style. 

